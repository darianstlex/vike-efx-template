import istanbul from 'istanbul-lib-coverage';

afterEach(() => {
  const coverageMap = istanbul.createCoverageMap({});

  cy.window().then((win) => {
    const coverage = win?.__coverage__;
    if (coverage) {
      coverageMap.merge(coverage);
      const name = Cypress?.spec?.baseName || Date.now();
      cy.writeFile(`.nyc_output/${name}.coverage.${Date.now()}.json`, JSON.stringify(coverageMap)).then(() => null);
    }
  });
});
